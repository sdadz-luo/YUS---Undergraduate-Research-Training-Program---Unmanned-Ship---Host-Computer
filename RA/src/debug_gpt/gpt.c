#include "gpt.h"

/*============================================================================*
 * 模块内部变量
 *============================================================================*/

/* 定时周期到标志 */
static volatile bool g_gpt_timeout = false;

/*============================================================================*
 * FSP 回调函数
 *============================================================================*/

/*******************************************************************************************************************//**
 * @brief GPT0 定时器周期回调（每 10ms 触发一次）
 **********************************************************************************************************************/
void gpt0_callback(timer_callback_args_t *p_args)
{
    if (TIMER_EVENT_CYCLE_END == p_args->event)
    {
        g_gpt_timeout = true;
    }
}

/*============================================================================*
 * 接口函数
 *============================================================================*/

/*******************************************************************************************************************//**
 * @brief 初始化 GPT 定时器
 *
 * 打开 GPT 定时器（周期模式，10ms），但不启动。
 * 启动需调用 Gpt_Start()。
 **********************************************************************************************************************/
void Gpt_Init(void)
{
    fsp_err_t err;

    err = g_timer0.p_api->open(g_timer0.p_ctrl, g_timer0.p_cfg);
    assert(FSP_SUCCESS == err);
}

/*******************************************************************************************************************//**
 * @brief 启动定时器
 **********************************************************************************************************************/
void Gpt_Start(void)
{
    g_timer0.p_api->start(g_timer0.p_ctrl);
}

/*******************************************************************************************************************//**
 * @brief 停止定时器
 **********************************************************************************************************************/
void Gpt_Stop(void)
{
    g_timer0.p_api->stop(g_timer0.p_ctrl);
}

/*******************************************************************************************************************//**
 * @brief 检查并清除定时周期标志
 * @return true=定时周期到
 **********************************************************************************************************************/
bool Gpt_TimeoutCheck(void)
{
    bool ret = g_gpt_timeout;
    if (ret)
    {
        g_gpt_timeout = false;
    }
    return ret;
}